"""Детектор маркетингового / zero-budget plan intent для triage override."""

from __future__ import annotations

import logging
import re

logger = logging.getLogger(__name__)

_MARKETING_RE = re.compile(
    r"(?:маркетинг\w*|marketing|реклам\w*|продвижен\w*|smm|контент[-\s]?план\w*|"
    r"воронк\w*|лидген\w*|lead\s*gen|brand\s*aware|узнаваем\w*)",
    re.IGNORECASE | re.UNICODE,
)
_PLAN_RE = re.compile(
    r"(?:план\w*|стратег\w*|календар\w*|расписан\w*|roadmap|playbook)",
    re.IGNORECASE | re.UNICODE,
)
_ZERO_BUDGET_RE = re.compile(
    r"(?:0\s*(?:руб|₽|rub)|за\s*0|без\s*бюджет\w*|бесплатн\w*|zero\s*budget|organic\s*only)",
    re.IGNORECASE | re.UNICODE,
)


def detect_marketing_plan_intent(text: str) -> bool:
    """True, если владелец просит маркетинговый/рекламный план (часто с нулевым бюджетом)."""
    raw = (text or "").strip()
    if not raw:
        return False
    has_mkt = bool(_MARKETING_RE.search(raw))
    has_plan = bool(_PLAN_RE.search(raw))
    has_zero = bool(_ZERO_BUDGET_RE.search(raw))
    # «маркетинговый-рекламный план» / «marketing plan» / «рекламный план за 0»
    result = bool(has_mkt and (has_plan or has_zero))
    logger.info(
        "MARKETING_PLAN_INTENT_CHECK: result=%s mkt=%s plan=%s zero=%s head=%r",
        result,
        has_mkt,
        has_plan,
        has_zero,
        raw[:200],
    )
    return result


